#!/bin/bash
# ============================================================
# 专辑元数据补充工具 - Shell 脚本版本
# ============================================================

# 设置脚本目录（本文件的父目录）
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"

# 默认输入目录（可以根据需要修改）
# 如果要选择特定专辑目录，直接修改这里
DEFAULT_INPUT_DIR="/g/QQMusic/Decrypted\VipSongsDownload"

# 检查 Python 是否可用
if ! command -v python &> /dev/null; then
    echo "============================================================"
    echo "   错误：未找到 Python"
    echo "============================================================"
    echo ""
    echo "请确保 Python 已安装并添加到 PATH 环境变量中"
    echo ""
    echo "下载地址：https://www.python.org/downloads/"
    echo ""
    read -p "按任意键退出..."
    exit 1
fi

# 显示欢迎信息
echo "============================================================"
echo "   专辑元数据补充工具"
echo "============================================================"
echo ""
echo "本工具将为 FLAC 文件添加："
echo "   1. 专辑封面（嵌入文件 + 保存为 cover.jpg）"
echo "   2. 发行年份（从 API 获取并写入 DATE 字段）"
echo ""
echo "使用方法："
echo "   1. Git Bash 中双击：使用默认目录"
echo "   2. 拖动目录到脚本上：处理拖动的目录"
echo "   3. 修改脚本中的 DEFAULT_INPUT_DIR 变量"
echo ""
echo "============================================================"
echo ""

# 检查是否有参数（拖动到脚本上）
INPUT_DIR="$1"

if [ -z "$INPUT_DIR" ]; then
    # 没有参数，使用默认目录
    INPUT_DIR="$DEFAULT_INPUT_DIR"
    echo "使用默认目录: $INPUT_DIR"
    echo ""
else
    # 有参数，使用拖动的目录
    echo "处理拖动的目录: $INPUT_DIR"
    echo ""
fi

# 检查目录是否存在
if [ ! -d "$INPUT_DIR" ]; then
    echo "============================================================"
    echo "   错误：目录不存在"
    echo "============================================================"
    echo ""
    echo "目录路径: $INPUT_DIR"
    echo ""
    echo "请检查路径是否正确，或者编辑本文件修改 DEFAULT_INPUT_DIR 变量"
    echo ""
    read -p "按任意键退出..."
    exit 1
fi

# 检查是否是文件（不是目录）
if [ -f "$INPUT_DIR" ]; then
    echo "============================================================"
    echo "   错误：提供的路径是文件，不是目录"
    echo "============================================================"
    echo ""
    echo "路径: $INPUT_DIR"
    echo ""
    echo "请提供目录路径，而不是文件路径"
    echo ""
    read -p "按任意键退出..."
    exit 1
fi

# 运行 Python 脚本
echo "开始处理..."
echo ""

python "$SCRIPT_DIR/supplement_album_metadata.py" "$INPUT_DIR" "$@"

# 检查执行结果
if [ $? -ne 0 ]; then
    echo ""
    echo "============================================================"
    echo "   处理失败，错误代码: $?"
    echo "============================================================"
    echo ""
    echo "请检查上面的错误信息"
    echo ""
else
    echo ""
    echo "============================================================"
    echo "   处理完成！"
    echo "============================================================"
    echo ""
    echo "已为 FLAC 文件添加："
    echo "   - 专辑封面（嵌入文件）"
    echo "   - 封面文件（保存为 cover.jpg）"
    echo "   - 发行年份（写入 DATE 字段）"
    echo ""
fi

read -p "按任意键退出..."
